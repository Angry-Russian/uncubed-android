public class Rect {
    public float x = 0;
    public float y = 0;
    public float w = 0;
    public float h = 0;
    public double r = 0;

    public Rect(float x, float y, float w, float h, double r){
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
        this.r = r;
    }
}
